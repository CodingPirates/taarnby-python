# Computere er gode til at regne, og det er Python selvfølgelig også
# Man kan lægge til, trække fra, dividere og gange (plus en masse andre ting)
# Når man regner, bruger man tegnet for plus og minus (ligesom i matematiktimerne)
# Tegnene kaldes for operatorer

# Nu lægger vi tal sammen

print("2 plus 2 er lig med")
print(2+2)
print("") # Her laver vi en tom linie, så vi bedre kan se resultaterne

# Læg lige mærke til at det som står i gåseøjne bliver skrevet ud som det står
# Det som ikke står i gåseøjne bliver regnet ud først, så skrevet ud.
# Det som står i gåseøjne kaldes en streng (det taler vi mere om senere)
# Den sidste print-kommando starter med at regne ud hvad to plus to er, og skriver resultatet ud på skærmen

# Vi kan også trække fra:

print("5 minus 2 er lig med ")
print(5-2)
print("") # Her laver vi en tom linie, så vi bedre kan se resultaterne

# Når vi ganger, så bruger vi en stjerne (også kald asteriks):

print("3 gange 3 er lig med")
print(3*3)
print # Her laver vi en tom linie, så vi bedre kan se resultaterne

# Når vi dividerer, bruger vi en skråstreg:

print("10 divideret med 5 er lig med ")
print(10/5)
print("")

# Når man regner stykker med plus og minus og gange og divider, så er det ikke ligemeget hvor man starter
# De kloge matematikere har på et tidspunkt blevet enige om, at man starter med at gange og dividere, og
# plus og minus kommer bagefter. Sådan gør computerne også
# Derfor, når man først skal plusse og efterfølgende gange op eller dividere, så skal man sætte parenteser rundt om det man gerne
# vil regne først:

# Hvis Berndt har 5 karameller, og Morten har 10 karameller, og de vil gerne dele med deres tre venner, hvor mange karameller får hver:
print("De får så mange karameller hver:")
print( (5+10)/(2+3) )  # over stregen er de 5 og 10 karameller, under stregen er det Berndt og Morten plus 3 venner

